package oop03;

public class Test01_data {

	public static void main(String[] args) {
		// 데이터의 종류

		// 1. 수치형
		System.out.println(5);    // 양의 정수
		System.out.println(-5);   // 음의 정수
		System.out.println(2.4);  // 실수
		System.out.println(-5.6); // 실수
		System.out.println(7.);   // 실수
		
		// 2. 문자형
		System.out.println("helloWorld");
		System.out.println("자바");
		System.out.println('A');
		System.out.println('가');
		System.out.println("123");

		// 3. 논리형
		System.out.println(true); //참
		System.out.println(false);//거짓

	}// end main()

}// end class
