package oop03;

public class Test02_variable {

	public static void main(String[] args) {
		// 상수와 변수
		
		// 1. 상수
		System.out.println(5);
		System.out.println("자바");
		
		// 2. 변수
		int a = 5; // 변수 선언
		System.out.println(a);
		System.out.println(a + 5); // 5 + 5
		
		a = 0;
		System.out.println(a);

	} //end main()

} // end class
